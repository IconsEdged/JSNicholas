/*the Logical NOT operator can also be used to convert to Boolean Value,
 * by using 2 logical NOT operator you can get the Boolean equivalent
 */

alert("!!false = " +(!!false));
alert("!!'blue' = " +(!!"blue"));
alert("!!0 = " +(!!0));
alert("!!NaN = " +(!!NaN));
alert("!!'' = " +(!!""));

